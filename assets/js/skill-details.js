function getCourseTimestamp(course) {
  if (course.date) {
    return Date.parse(`${course.date}T00:00:00Z`);
  }

  const monthNames = {
    january: 0,
    february: 1,
    march: 2,
    april: 3,
    may: 4,
    june: 5,
    july: 6,
    august: 7,
    september: 8,
    october: 9,
    november: 10,
    december: 11,
  };
  const monthYear = course.details.match(
    /(January|February|March|April|May|June|July|August|September|October|November|December)\s+(\d{4})/i,
  );

  if (monthYear) {
    return Date.UTC(
      Number(monthYear[2]),
      monthNames[monthYear[1].toLowerCase()],
      1,
    );
  }

  const year = course.details.match(/\b(20\d{2})\b/);
  return year ? Date.UTC(Number(year[1]), 0, 1) : Number.NEGATIVE_INFINITY;
}

