function setText(id, value) {
  document.getElementById(id).textContent = value;
}

function renderProject(project) {
  document.title = `${project.title} - Kyska Harrington`;
  document
    .getElementById("project-meta-description")
    .setAttribute("content", project.summary);

  setText("project-title", project.title);
  setText("project-summary", project.summary);
  setText("project-breadcrumb", project.title);
  setText("project-heading", project.title);
  setText("project-description", project.description);
  setText("project-challenge", project.challenge);
  setText("project-solution", project.solution);
  setText("project-category", project.category);
  setText("project-role", project.role);
  setText("project-technologies", project.technologies);
  setText("project-link-label", project.linkLabel);

  const image = document.getElementById("project-image");
  image.src = project.image;
  image.alt = project.title;

  const projectLink = document.getElementById("project-link");
  projectLink.href = project.link;

  const highlights = document.getElementById("project-highlights");
  project.highlights.forEach((highlight) => {
    const item = document.createElement("li");
    const icon = document.createElement("i");
    icon.className = "bi bi-check-circle";
    item.append(icon, document.createTextNode(highlight));
    highlights.appendChild(item);
  });
}

function showNotFound() {
  document.getElementById("project-content").classList.add("d-none");
  document.getElementById("project-not-found").classList.remove("d-none");
  setText("project-title", "Project Not Found");
  setText(
    "project-summary",
    "The requested case study is unavailable. Return to the projects page to continue browsing.",
  );
}

const projectId = new URLSearchParams(window.location.search).get("project");
const selectedProject = projects[projectId];

if (selectedProject) {
  renderProject(selectedProject);
} else {
  showNotFound();
}
